using System.ComponentModel.DataAnnotations;

namespace pms.Services.Models.Authentication.SignUp
{
    public class SignUp
    {
        [Required]
        public string Username { get; set; } = null!;

        [Required]
        [EmailAddress]
        public string Email { get; set; } = null!;

        [Required]
        public string Password { get; set; } = null!;
    }
}
